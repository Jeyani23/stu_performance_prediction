import streamlit as st
import numpy as np
import pickle

with open('student_performance_prediction.pkl', 'rb') as file:
    model = pickle.load(file)

gender_map = {'Male': 1, 'Female': 0}
parentedu_map = {
    'High School': 0,
    'Bachelor': 1,
    'Master': 2,
    'PhD': 3
}
extra_map = {'Yes': 1, 'No': 0}

st.title("ðŸ“˜ Student Performance Predictor")
st.markdown("This app predicts a student's final exam score based on academic and background factors.")

gender = st.selectbox("Gender", list(gender_map.keys()))
study_hours = st.slider("Study Hours per Week", 0, 40, 10)
past_scores = st.slider("Past Exam Score Average (%)", 0, 100, 75)
parent_edu = st.selectbox("Parental Education Level", list(parentedu_map.keys()))
extracurricular = st.selectbox("Extracurricular Activities", list(extra_map.keys()))

if st.button("Predict Final Exam Score"):
    input_data = np.array([[
        gender_map[gender],
        study_hours,
        past_scores,
        parentedu_map[parent_edu],
        extra_map[extracurricular]
    ]])

    prediction = model.predict(input_data)[0]

    st.success(f"ðŸŽ¯ Predicted Final Exam Score: {round(prediction, 2)} / 100")